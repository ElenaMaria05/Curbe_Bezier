t=0:.01:1;
b0=[31;-2.13];
b1=[33.5;2.9];
b2=[33;2.5];
b3=[33;8];
b=[b0 b1 b2 b3];
B0=(1-t).^3;
B1=3*(1-t).^2.*t;
B2=3*(1-t).*(t.^2);
B3=t.^3;
B=[B0;B1;B2;B3];
f=b*B;
hold on
plot(f(1,:),f(2,:),'k',LineWidth=1)
j=1/3;
b01=b0*(1-j)+b1*j;
b11=b1*(1-j)+b2*j;
b21=b2*(1-j)+b3*j;
b02=b01*(1-j)+b11*j;
b12=b11*(1-j)+b21*j;
b03=b02*(1-j)+b12*j;
bn1=[b01 b11 b21];
bn2=[b02 b12];
bn3=b03;
plot(b(1,:),b(2,:),'b-')
plot(bn1(1,:),bn1(2,:),'r*-')
plot(bn2(1,:),bn2(2,:),'r*-')
plot(bn3(1,:),bn3(2,:),'*r-')
hold off